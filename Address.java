package adminModule;

public class Address {

	private int wodaNo;
	private int provinceNo;
	private String provinceNAme;
	private String cityName;
	private String toleName;
	private String zone;
	
	public int getWodaNo() {
		return wodaNo;
	}
	public void setWodaNo(int wodaNo) {
		this.wodaNo = wodaNo;
	}
	public int getProvinceNo() {
		return provinceNo;
	}
	public void setProvinceNo(int provinceNo) {
		this.provinceNo = provinceNo;
	}
	public String getProvinceNAme() {
		return provinceNAme;
	}
	public void setProvinceNAme(String provinceNAme) {
		this.provinceNAme = provinceNAme;
	}
	public String getCityName() {
		return cityName;
	}
	public void setCityName(String cityName) {
		this.cityName = cityName;
	}
	public String getToleName() {
		return toleName;
	}
	public void setToleName(String toleName) {
		this.toleName = toleName;
	}
	public String getZone() {
		return zone;
	}
	public void setZone(String zone) {
		this.zone = zone;
	}

}
